    private void bntCadastraUniversalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCadastraUniversalActionPerformed
        String nome, sinopse, tipo ,tituloal, nomeEdes, nomeEsc, link, cat, capitulo, tiEdes, genero, sta;
        
        nome = textNome.getText();
        sinopse = textSinopset.getText();
        tipo = tipoid.getSelection().toString();
        tituloal = textTituloAl.getText();
        nomeEdes = textNomeEdes.getText();
        tiEdes = tipoEdes.getSelection().toString();
        nomeEsc = textEscritor.getText();
        genero = sexo.getSelection().toString();
        sta = status.getSelection().toString();
        link = textLink.getText();
        cat = textCategoria.getText();
        capitulo = textCapitulo.getText();
        
        JOptionPane.showMessageDialog(rootPane, tipoid.getSelection());
        JOptionPane.showMessageDialog(rootPane, tipoEdes.getSelection());
        JOptionPane.showMessageDialog(rootPane, sexo.getSelection());
        JOptionPane.showMessageDialog(rootPane, status.getSelection());
        
        Categoria categoria = new Categoria();
        categoria.setNome(cat);
        
        CategoriaDAO categoriadao = new CategoriaDAO();
        categoriadao.CadastrarCategoria(categoria);
        
        Universal universal = new Universal();
        universal.setNome(nome);
        universal.setSinopse(sinopse);
        universal.setTipo(tipo);
        universal.setCapitulos(capitulo);
        universal.setStatus(sta);
        universal.setNome_alternativo(tituloal);
        universal.setLink(link);
        
        UniversalDAO universaldao = new UniversalDAO();
        universaldao.CadastrarUniversal(universal);
        
        EditoraEstudio edes = new EditoraEstudio();
        edes.setNome(nomeEdes);
        edes.setTipo(tipo);
        
        EditoraEstudioDAO eddao = new EditoraEstudioDAO();
        eddao.CadastrarEditoraEstudio(edes);
        
        Escritor esc = new Escritor();
        esc.setNome(nomeEsc);
        esc.setSexo(genero);
        
        
        EscritorDAO escritordao = new EscritorDAO();
        escritordao.CadastrarEscritor(esc);
        
    }//GEN-LAST:event_bntCadastraUniversalActionPerformed

